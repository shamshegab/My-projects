    @Override
    public ResultSet executeQuery(String sql) throws SQLException {
         String queries[] = sql.split("[ (,)]+|[ ( );]");
         int i=0;
         for (int j = 0; j < queries.length; j++) {
            if(queries[i].equalsIgnoreCase("FROM")){
                break;
            }
        }
        ResultSet resultSet = new jdbc.ResultSet((String[][]) DBManager.getInstance().executeRetrievalQuery(sql),queries[i+1]);
        
        return resultSet;
    }
 
    @Override
    public int executeUpdate(String sql) throws SQLException {
        return DBManager.getInstance().executeUpdateQuery(sql);
    }

    @Override
    public boolean execute(String sql) throws SQLException {
        if(sql.contains("CREATE")||sql.contains("create")||sql.contains("DROP")||sql.contains("drop")){
           return DBManager.getInstance().executeStructureQuery(sql);
            
        }else if(sql.contains("SELECT")||sql.contains("select")){
            DBManager.getInstance().executeRetrievalQuery(sql);
            return true;
        }else if(sql.contains("INSERT")||sql.contains("insert")||sql.contains("DELETE")||sql.contains("delete")||sql.contains("UPDATE")||sql.contains("update")){
            DBManager.getInstance().executeUpdateQuery(sql);
            return true;
        }else{
            return false;
        }
    }

